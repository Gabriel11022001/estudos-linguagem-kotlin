package com.gabriel.estudoskotlin.classes

class Calculadora {

    fun somar(primeiroValor: Double, segundoValor: Double): Double {
        return primeiroValor + segundoValor;
    }
    fun dividir(primeiroValor: Double, segundoValor: Double): Double {
        return primeiroValor / segundoValor;
    }
    fun subtrair(primeiroValor: Double, segundoValor: Double): Double {
        return primeiroValor - segundoValor;
    }
    fun multiplicar(primeiroValor: Double, segundoValor: Double): Double {
        return primeiroValor * segundoValor;
    }
}